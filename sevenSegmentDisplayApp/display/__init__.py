class SegmentRenderer:
    SEGMENTS = [
        ["####", "#  #", "#  #", "#  #", "####"],
        ["    ", "   #", "   #", "   #", "    "],
        ["####", "   #", "####", "#   ", "####"],
        ["####", "   #", "####", "   #", "####"],
        ["    ", "#  #", "####", "   #", "    "],
        ["####", "#   ", "####", "   #", "####"],
        ["####", "#   ", "####", "#  #", "####"],
        ["####", "   #", "   #", "   #", "    "],
        ["####", "#  #", "####", "#  #", "####"],
        ["####", "#  #", "####", "   #", "####"]
    ]

    def render(self, digit):
        if 0 <= digit <= 9:
            for line in self.SEGMENTS[digit]:
                print(line)
        else:
            print("Invalid digit! Cannot render.")
