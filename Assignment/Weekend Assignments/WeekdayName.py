dayNumber = int(input("Input number: "))

if dayNumber == 1:
    dayName = "Sunday"
elif dayNumber == 2:
    dayName = "Monday"
elif dayNumber == 3:
    dayName = "Tuesday"
elif dayNumber == 4:
    dayName = "Wednesday"
elif dayNumber == 5:
    dayName = "Thursday"
elif dayNumber == 6:
    dayName = "Friday"
elif dayNumber == 7:
    dayName = "Saturday"
else:
    dayName = "Invalid input"

print(dayName)

